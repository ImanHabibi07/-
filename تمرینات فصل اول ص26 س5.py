# برنامه ای بنویسید که M و N ( به شرط M>N ) را از ورودی دریافت کند و سری غیبوناچی بین انها را در خروجی چاپ کند.
M = int(input("عدد بزرگتر را وارد کنید : "))
N = int(input("عدد کوچکتر را وارد کنید : "))
S = 0
i = 0
j = 1
while j <= N:
    S = j + i
    i = j
    j = S
temp = j
S=0
while j < M :
    temp =temp + S
    S = j + i
    i = j
    j = S
