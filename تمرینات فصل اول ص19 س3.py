# عددی از ورودی دریافت کند و بخش پذیری ان بر 3 و 5 را بررسی کند.
x = int(input("عدد مورد نظر را وارد کنید : "))
a = x%3
b = x%5
if a==0 and b==0:
    print (" عدد بر 5 و 3 بخش پذیر است.")
elif a==0 and b >0:
    print ("عدد بر 3 بخش پذیر است ولی بر 5 بخش پذیر نیست.")
elif a>0 and b==0:
    print ("عدد بر 5 بخش پذیر است ولی بر 3 بخش پذیر نیست.")
else :
    print ("عدد نه بر 3 و نه بر 5 بخش پذیر نیست.")