#برنامه ای بنویسید که سه عدد از ورودی دریافت کند ، جمع سه عدد و میانگین را حساب کند.
x = int ( input ( "عدد اول را وارد کنید : "))
y = int ( input ( "عدد دوم را وارد کنید : "))
z = int ( input ( "عدد سوم را وارد کنید : "))
sum = x+y+z
avg = (x+y+z)/3
print ( "مجموع سه عدد برابر است با:" , sum)
print ( "میانگین سه عدد برابر است با :" , avg )