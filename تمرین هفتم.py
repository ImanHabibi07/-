# برنامه ای بنویسید که طول و عرض مستطیل را دریافت کند و محیط و مساحت ان را حساب کند.
x = int ( input ("طول مستطیل را وارد کنید : "))
y = int ( input ("عرض مستطیل را وارد کنید : "))
P = 2 * (x+y)
S = x*y
print ( "محیط مستطیل برابر است با :" , P )
print ( "مساحت مستطیل برابر است با :" , S )